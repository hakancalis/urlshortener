function SendMail($receiver, $subject, $name_surname, $content) {
    $mail = new PHPMailer(true);

    $mail->SMTPDebug = 0;
    $mail->isSMTP();
    $mail->Host       = 'mail.domain.com'; // This field enter your SMTP host
    $mail->SMTPAuth   = true;
    $mail->Username   = 'user@mail.com'; // This field enter your web mail
    $mail->Password   = 'password'; // This field enter your web mail password
    $mail->SMTPSecure = PHPMailer::ENCRYPTION_SMTPS;
    $mail->Port       = 465;
    $mail->CharSet = "UTF-8";
    $mail->IsHTML(true);
    $mail->setFrom('user@mail.com', 'Sender Name'); // This field enter sender mail and sender name
    $mail->addAddress($receiver, $name_surname); // Do not change this field
    $mail->Subject = $subject;  // Do not change this field
    $mail->Body    = $content;  // Do not change this field
    $mail->send();
    }

function encrypt($string, $key) {
